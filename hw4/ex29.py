people = 20
cats = 30
dogs = 15

if people < cats:
    print('Дофига кошек - мир обречен')

if people > cats:
    print("Не так много кошек, все норм")

if people < dogs:
    print("Мир потом в слюнях")

if people > dogs:
    print("Все норм")

dogs += 5

if people >= dogs:
    print("Людей больше или столько же, сколько собак")

if people  <= dogs:
    print("Людей меньше или столько же, сколько собак")

if people >= dogs:
    print("Людей или собак столько же")