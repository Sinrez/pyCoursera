s = str(input())
print(s[2])  # третий символ
print(s[-2])  # предпоследний
print(s[:5])  # первые пять
print(s[:-2])  # все кроме последних двух
print(s[::2])  # все четные
print(s[1::2])  # все нечетные
print(s[::-1])  # в обратном порядке
print(s[::-2])  # в обратном порядке через 1
print(len(s))  # длина строки
